time_1 = float(input("Nhập vào thời gian về đích VĐV 1: "))
time_2 = float(input("Nhập vào thời gian về đích VĐV 2: "))
time_3 = float(input("Nhập vào thời gian về đích VĐV 3: "))
avg = (time_1 + time_2 + time_3)/3
print(f"Thời gian trung  bình cộng về đích của 3 VĐV: {avg}")
print("Thời gian trung  bình cộng về đích của 3 VĐV: ", round(avg,2)) # LẤY 2 SỐ DƯ